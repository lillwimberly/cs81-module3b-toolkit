function double(n) {
  return n + n;
}

const square = (n) => n * n;

const isEven = (n) => {
  if (n % 2 === 0) return true;
  return false;
};

function isOdd(num) {
  if (num % 2 !== 0) return true;
  else return false;
}

const multiply = (num1, num2) => {
  return num1 * num2;
};

const divide = (num, divisor) => {
  return num / divisor;
};
